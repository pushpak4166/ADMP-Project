ASSUME CS:CODE, DS:DATA

DATA SEGMENT
BLOCK1 DB 12H, 15H, 34H, 47H, 0CAH, 35H, 92H, 21H, 26H, 32H
BLOCK2 DB 13H, 17H, 34H, 26H, 92H, 35H, 99H, 0A1H, 16H, 39H
BLOCKSIZE DB 0AH
RESULT DB 01H DUP(?)
DATA ENDS

CODE SEGMENT
START:
MOV AX, DATA
MOV DS, AX
MOV ES, AX
MOV SI, OFFSET BLOCK1
MOV DI, OFFSET BLOCK2
CLD
MOV BL, 00H
MOV DL, 0AH
L2:
MOV CX, 000AH
L1:
MOV AL, [SI]
SCASB
JNZ SKIP
INC BL
SKIP:
LOOP L1
INC SI
MOV DI, OFFSET BLOCK2
DEC DL
JNZ L2
MOV DI, OFFSET RESULT
MOV [DI], BL
MOV AH, 4CH
INT 21H
CODE ENDS
END START
END




